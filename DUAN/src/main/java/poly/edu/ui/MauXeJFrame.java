    private void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblMauXe.getModel();
        model.setRowCount(0);
        try {
            
            List<MauXe> list = dao.selectAll();
            for (MauXe mx : list) {
                Object[] row = {mx.getMaMau(),mx.getMauXe()};
                model.addRow(row);
            }
        } catch (Exception e) {
            DiaLogHelper.alert(this, e.getMessage());
        }
    }

