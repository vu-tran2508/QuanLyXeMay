    private void setForm(MauXe mx){
        txtMaMau.setText(mx.getMaMau());
        txtTenMau.setText(mx.getMauXe());
    }
    private MauXe getForm(){
        MauXe mx = new MauXe();
        mx.setMaMau(txtMaMau.getText());
        mx.setMauXe(txtTenMau.getText());
        return mx;
    }
    private void clearForm(){
        MauXe mx = new MauXe();
    }
    private void insert(){
        MauXe entity = getForm();
        try {
            dao.insert(entity);
            this.fillTable();
            DiaLogHelper.alert(this, "Ðã thêm thành công!");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this,e.getMessage() ,"Lôi!!");
        }
    }
    private void delete(){
        if (DiaLogHelper.confirm(this, "B?n có mu?n xóa màu này không?")) {
            String mamau = txtMaMau.getText();
            try {
                dao.delete(mamau);
                this.fillTable();
                DiaLogHelper.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                DiaLogHelper.showErrorDialog(this, e.getMessage() ,"Xóa th?t b?i");
            }
        }
    }
    private void update(){
        MauXe mx = getForm();
        
        try {
            dao.update(mx);
            this.fillTable();
            DiaLogHelper.alert(this, "Xóa thành công!");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "L?i!!");
        }
    }
    private void edit(){
        String mamau = (String) tblMauXe.getValueAt(this.row, 0);
        MauXe mx = dao.selectById(mamau);
        this.setForm(mx);
    }
    private void first() {
        this.row = 0;
        this.edit();

    }

    private void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }

    }

    private void next() {
        if (this.row < tblMauXe.getRowCount() - 1) {
            this.row++;
            this.edit();
        }

    }

    private void last() {
        this.row = tblMauXe.getColumnCount() - 1;
        this.edit();

    }
     boolean flag=false;
    void check() {
        if ((txtMaMau.getText()).length() < 7 || (txtMaMau.getText()).length() > 7) {
            if ((txtMaMau.getText()).equals("")) {
                DiaLogHelper.alert(this, "Mã người học không được để trống");
            } else {
                DiaLogHelper.alert(this, "Mã người học phải nhập đúng 7 ký tự");
            }
        } else if ((txtTenMau.getText()).equals("")) {
            DiaLogHelper.alert(this, "Họ tên không được để trống");
        }
}
