    private void setForm(LoaiXe lx){
        txtMaLoai.setText(lx.getMaLoai());
        txtTenLoai.setText(lx.getTenLoai());
        txtThongTin.setText(lx.getThongtinloai());
    }
    private LoaiXe getForm(){
        LoaiXe lx = new LoaiXe();
        lx.setMaLoai(txtMaLoai.getText());
        lx.setTenLoai(txtTenLoai.getText());
        lx.setThongtinloai(txtThongTin.getText());
        return lx;
    }
    private void clearForm(){
        LoaiXe lx = new LoaiXe();
    }
    private void insert(){
        LoaiXe entity = getForm();
        try {
            dao.insert(entity);
            this.fillTable();
            DiaLogHelper.alert(this, "Ðã thêm thành công!");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this,e.getMessage() ,"Lôi!!");
        }
    }
    private void delete(){
        if (DiaLogHelper.confirm(this, "B?n có mu?n xóa màu này không?")) {
            String maloai = txtMaLoai.getText();
            try {
                dao.delete(maloai);
                this.fillTable();
                DiaLogHelper.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                DiaLogHelper.showErrorDialog(this, e.getMessage() ,"Xóa th?t b?i");
            }
        }
    }
    private void update(){
        LoaiXe lx = getForm();
        
        try {
            dao.update(lx);
            this.fillTable();
            DiaLogHelper.alert(this, "Xóa thành công!");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "L?i!!");
        }
    }
    private void edit(){
        String maloai = (String) tblLoaiXe.getValueAt(this.row, 0);
        LoaiXe lx = dao.selectById(maloai);
        this.setForm(lx);
    }
