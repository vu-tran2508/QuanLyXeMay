    private void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblLoaiXe.getModel();
        model.setRowCount(0);
        try {
            
            List<LoaiXe> list = dao.selectAll();
            for (LoaiXe lx : list) {
                Object[] row = {lx.getMaLoai(),lx.getTenLoai(),lx.getThongtinloai()};
                model.addRow(row);
            }
        } catch (Exception e) {
            DiaLogHelper.alert(this, e.getMessage());
        }
    }
