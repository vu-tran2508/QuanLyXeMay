    public void fillComboBoxHangXe() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboHangXe.getModel();
        cboHangXe.removeAllItems();
        List<HangXe> list = hangdao.selectAll();
        for (HangXe h : list) {
            model.addElement(h);
        }
    }

    void fillComboBoxMauXe() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboMauXe.getModel();
        cboMauXe.removeAllItems();
        List<MauXe> list = maudao.selectAll();
        for (MauXe m : list) {
            model.addElement(m);
        }
    }

    void fillComboBoxLoaiXe() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiXe.getModel();
        cboLoaiXe.removeAllItems();
        List<LoaiXe> list = loaidao.selectAll();
        for (LoaiXe v : list) {
            model.addElement(v);
        }
    }

    private void insert() {
        SanPham ttx = getForm();
        try {
            dao.insert(ttx);
            this.fillTable();
            this.clearFrom();
            DiaLogHelper.alert(this, "Thêm Mới Thành Công");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "Thêm Mới Thất Bại!");
        }
    }

    private void update() {
        SanPham ttx = getUpdate();
        try {
            dao.update(ttx);
            this.fillTable();
            this.clearFrom();
            DiaLogHelper.alert(this, "Cập Nhật Thành Công");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "Cập nhật Thất Bại!");
        }
    }

    public void delete() {
        if (DiaLogHelper.confirm(this, "Bạn có muốn xóa hoa don này ?")) {
            Integer makh = (Integer) tblThongTinXe.getValueAt(this.index, 0);
            try {
                dao.delete(makh);
                this.fillTable();
                this.clearFrom();
                DiaLogHelper.alert(this, "Xóa Thành Công");
            } catch (Exception e) {
                DiaLogHelper.showErrorDialog(this, e.getMessage(), "Xóa Thật Thất Bại!");
            }
        }
    }

    private void clearFrom() {
        SanPham ttx = new SanPham();
        this.setForm(ttx);

    }

    private void edit() {
        Integer manv = (Integer) tblThongTinXe.getValueAt(this.index, 0);
        SanPham ttx = dao.selectById(manv);
        this.setForm(ttx);
    }

    private void chonAnh() {
        if (FileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = FileChooser.getSelectedFile();
            ImageHelper.save(file);
            ImageIcon icon = ImageHelper.read(file.getName());
            lblHinh.setIcon(icon);
            lblHinh.setToolTipText(file.getName());
        }
    }
