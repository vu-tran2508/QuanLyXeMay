    private void fillTable() { // đổ dữ liệu lên Table
        DefaultTableModel model = (DefaultTableModel) tblThongTinXe.getModel();  // tao nơi chưa dữ liệu hiện lên table
        model.setRowCount(0);        
        try {
            List<SanPham> list = dao.selectAll();
            for (SanPham ttx : list) {
                Object[] row = {
                    ttx.getMaXe(),
                    ttx.getTenXe(),
                    ttx.getTenHang(),
                    ttx.getTenMau(),
                    ttx.getSoMay(),
                    ttx.getGia(),
                    ttx.getLoaiXe(),
                    ttx.getSoKhung(),
                    ttx.getDungTich(),
                    ttx.getSoLuong(),
                    ttx.getHinh()};
                model.addRow(row); //bỏ vào từng hàng của bảng
            }
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "Lỗi Truy Vấn Dữ Liệu !");
        }
        
    }
