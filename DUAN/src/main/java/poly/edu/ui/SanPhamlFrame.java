    private void setForm(SanPham ttx) {
        txtTenXe.setText(ttx.getTenXe());
        String mahang = (String) tblThongTinXe.getValueAt(this.index, 2);
        HangXe hx = hangdao.selectById(mahang);
        cboHangXe.setSelectedItem(hx);
        String mamau = (String) tblThongTinXe.getValueAt(this.index, 3);
        MauXe mx = maudao.selectById(mamau);
        cboMauXe.setSelectedItem(mx);
        txtSoMay.setText(ttx.getSoMay());
        txtGia.setText(ttx.getGia() + "");
        String maloai = (String) tblThongTinXe.getValueAt(this.index, 6);
        LoaiXe lx = loaidao.selectById(maloai);
        cboLoaiXe.setSelectedItem(lx);
        txtSoKhung.setText(ttx.getSoKhung());
        txtDungTich.setText(ttx.getDungTich());
        txtSoLuong.setText(ttx.getSoLuong() + "");
        if (ttx.getHinh() != null) {
            lblHinh.setToolTipText(ttx.getHinh());
            lblHinh.setIcon(ImageHelper.read(ttx.getHinh()));
        }

    }

    private SanPham getForm() { // tạo 1 nhanvien từ form
        SanPham ttx = new SanPham();

        ttx.setTenXe(txtTenXe.getText());
        HangXe hx = (HangXe) cboHangXe.getSelectedItem();
        ttx.setTenHang(hx.getMaHang());
        MauXe mx = (MauXe) cboMauXe.getSelectedItem();
        ttx.setTenMau(mx.getMaMau());
        ttx.setSoMay(txtSoMay.getText());
        ttx.setGia(Float.valueOf(txtGia.getText()));
        LoaiXe lx = (LoaiXe) cboLoaiXe.getSelectedItem();
        ttx.setLoaiXe(lx.getMaLoai());
        ttx.setSoKhung(txtSoKhung.getText());
        ttx.setDungTich(txtDungTich.getText());
        ttx.setSoLuong(Integer.valueOf(txtSoLuong.getText()));
        ttx.setHinh(lblHinh.getToolTipText());
        return ttx;
    }

