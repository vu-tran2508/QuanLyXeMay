    public boolean checkTrungMa(JTextField txt) {
        txt.setBackground(white);
        if (dao.selectById(txt.getText()) == null) {
            return true;
        } else {
            txt.setBackground(pink);
            DiaLogHelper.alert(this, txt.getName() + " đã bị tồn tại.");
            return false;
        }
    }
