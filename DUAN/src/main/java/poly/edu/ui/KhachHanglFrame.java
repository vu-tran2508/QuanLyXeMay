    private void initTable() {
        model = new DefaultTableModel();
        model.setColumnIdentifiers(new String[]{"Mã KH", "Tên","Giới Tính","Ðịa Chỉ","SĐT","Ngày Lập"});
        tblKhachHang.setModel(model);
    }
    private void fillTable(){ // đổ dữ liệu lên Table
       DefaultTableModel model =(DefaultTableModel) tblKhachHang.getModel();  // tao nơi chưa dữ liệu hiện lên table
         model.setRowCount(0); 
         try {
             List<KhachHang>list=dao.selectAll();
             for (KhachHang kh : list) {
                 Object[] row ={kh.getMaKH(),kh.getTenKH(),kh.isGioiTinh()? "Nam":"Nu" ,kh.getDiaChi(),kh.getSDT(),DateHelper.toString(kh.getNgaycapnhat(), "MM/dd/yyyy")};
                 model.addRow(row); //bỏ vào từng hàng của bảng
             }
         } catch (Exception e) {
             DiaLogHelper.showErrorDialog(this,e.getMessage(), "Lỗi Truy Vấn Dữ Liệu !");
         }
         
     }
    private void setForm(KhachHang kh){ 
         txtMaKH.setText(kh.getMaKH()); 
         txtTenKH.setText(kh.getTenKH());
         rdoNam.setSelected(kh.isGioiTinh());
         rdoNu.setSelected(!kh.isGioiTinh());
         txtDiaChi.setText(kh.getDiaChi());
         txtSDT.setText(kh.getSDT());
     }
    private KhachHang  getForm(){ // tạo 1 nhanvien từ form
        KhachHang kh= new KhachHang();
        kh.setMaKH(txtMaKH.getText()); // lấy toàn bộ thông tin của form bỏ vào các thucocj tinh của nhân viên  
        kh.setTenKH(txtTenKH.getText());
        kh.setGioiTinh(rdoNam.isSelected());
        kh.setDiaChi(txtDiaChi.getText());
        kh.setSDT(txtSDT.getText());
        java.util.Date date = new java.util.Date();
        kh.setNgaycapnhat(date);
        return kh;
         
    }
    private void init(){
    this.fillTable();
    this.row=-1;
}
    private void insert(){
    KhachHang kh =getForm();
    try {
            dao.insert(kh);this.fillTable();this.clearFrom();
            DiaLogHelper.alert(this, "Thêm Mới Thành Công");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "Thêm Mới Thất Bại!");
        }
    }
    private void update(){
    KhachHang kh =getForm();
       try {
            dao.update(kh);this.fillTable();
            DiaLogHelper.alert(this, "Cập Nhật Thành Công");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "Cập nhật Thất Bại!");
        }
    }
    private void delete(){
    if(DiaLogHelper.confirm(this, "Bạn có muốn xóa nhân viên này ?")){
    String makh= txtMaKH.getText();
     try {
           dao.delete(makh);this.fillTable();this.clearFrom();
           DiaLogHelper.alert(this, "Xóa Thành Công");
        } catch (Exception e) {
            DiaLogHelper.showErrorDialog(this, e.getMessage(), "Xóa Thật Thất Bại!");
     }
}
}
    private void clearFrom(){
    KhachHang kh = new KhachHang();
    this.setForm(kh);
    this.row=-1;   
}
    private void edit(){
    String manv=(String) tblKhachHang.getValueAt(this.row, 0); 
    KhachHang kh = dao.selectById(manv);
    this.setForm(kh);  
}
